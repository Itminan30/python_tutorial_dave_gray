# Various Mathematical Operators
print("Mathematical Operators:")
math = (2 + 2)
print(math)
math = (2 - 2)
print(math)
math = (2 * 2)
print(math)
math = (2 / 2)
print(math)
math = (24 / 5)
print(math)
math = (24 // 5)  # Floor value operator
print(math)
math = round(24 / 5)  # Rounds up the value
print(math)

# Comparasion Operators
print("Comparasion Operators:")
print(42 == 41)
print(42 == 42.0)
print(42 == "42")
print(10 >= 10)

# Boolean Operators
print("Boolean Operators:")
x = True
y = False
z = True
print(not x)
print(x and y)
print(x and z)
print(x or y)
print(x or z)
print(y or y)
